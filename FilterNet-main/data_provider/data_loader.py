class Dataset_Custom(Dataset):
    def __init__(self, root_path, flag='train', size=None,
                 features='S', data_path='ETTh1.csv',
                 target='cnt', scale=True, timeenc=0, freq='h', seasonal_patterns=None):
        # size [seq_len, label_len, pred_len]
        # 数据路径， 数据集划分的类别 ，尺寸，特征类型，数据集名字，预测值的类别，缩放器，timeenc ， 频率
        # info
        if size == None:
            self.seq_len = 24 * 4 * 4 # 序列长度 96*4 =384
            self.label_len = 24 * 4 # 标签长度 ，15min 变一个小时？
            self.pred_len = 24 * 4 #  预测长度
        else:
            self.seq_len = size[0] # 自定义给予的
            self.label_len = size[1]
            self.pred_len = size[2]
        # init
        assert flag in ['train', 'test', 'val'] # 如果flag 在这三种里
        type_map = {'train': 0, 'val': 1, 'test': 2} # 对应的数字
        self.set_type = type_map[flag] # 获得对应的数据集类型

        self.features = features # 数据特征
        self.target = target # 数据目标的标签
        self.scale = scale # 重缩放
        self.timeenc = timeenc # 时间超参
        self.freq = freq # 频率

        self.root_path = root_path # 数据子路径
        self.data_path = data_path # 数据集名字
        self.__read_data__() # 读取数据

    def __read_data__(self):
        self.scaler = StandardScaler() # 重缩放器
        df_raw = pd.read_csv(os.path.join(self.root_path,
                                          self.data_path))  # 读取数据集

        '''
        df_raw.columns: ['date', ...(other features), target feature]
        '''
        cols = list(df_raw.columns) #  获取数据集的类别列表
        if self.features == 'S':  # 如果是S类型， 把标签去掉
            cols.remove(self.target)
        cols.remove('dteday') # 移除日期
        # df_raw = df_raw[['date'] + cols + [self.target]]
        num_train = int(len(df_raw) * 0.750) # 训练集大小
        num_test = 2160 # 测试集大小
        num_vali = len(df_raw) - num_train - num_test # 验证集大小
        border1s = [0, num_train - self.seq_len, len(df_raw) - num_test - self.seq_len] #
        border2s = [num_train, num_train + num_vali, len(df_raw)]
        border1 = border1s[self.set_type] # 数据集开头和结尾，训练[0,train_len]  ,验证[train_len - seq_len ,train_len + num_Valid]
        border2 = border2s[self.set_type] # 测试：[all - test_num - seq_len ,all]

        if self.features == 'M' or self.features == 'MS':
            cols_data = df_raw.columns[2:]
            df_data = df_raw[cols_data] # MS ,多元预测单变量
        elif self.features == 'S':
            df_data = df_raw[[self.target]]

        if self.scale:
            train_data = df_data[border1s[0]:border2s[0]]
            self.scaler.fit(train_data.values)  # 把训练集正则化
            data = self.scaler.transform(df_data.values)  # 转化
        else:
            data = df_data.values
        
        df_stamp = df_raw[['dteday']][border1:border2]
        df_stamp['dteday'] = pd.to_datetime(df_stamp.dteday)
        if self.timeenc == 0:
            df_stamp['month'] = df_stamp.dteday.apply(lambda row: row.month, 1)
            df_stamp['day'] = df_stamp.dteday.apply(lambda row: row.day, 1)
            df_stamp['weekday'] = df_stamp.dteday.apply(lambda row: row.weekday(), 1)
            data_stamp = df_stamp.drop(['dteday'], 1).values
        elif self.timeenc == 1:
            data_stamp = time_features(pd.to_datetime(df_stamp['dteday'].values), freq=self.freq)
            data_stamp = data_stamp.transpose(1, 0)
            
        self.data_x = data[border1:border2]
        self.data_y = data[border1:border2]
        self.data_stamp = data_stamp

    def __getitem__(self, index):
        s_begin = index # 索引
        s_end = s_begin + self.seq_len # 索引 + 序列长度
        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len

        seq_x = self.data_x[s_begin:s_end] # 训练长度
        seq_y = self.data_y[r_begin:r_end] # 测试长度
